function toggle0() {
  var vGuias = document.getElementById('conteudo');
  if (vGuias.style.display == "block") {
    // se vGuias estiver escondido, exiba-o
    vGuias.style.display = "none";
  } else {
    // se vGuias for exibido, esconda-o
    vGuias.style.display = "block";
  }
}

